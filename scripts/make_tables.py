# ---------------------------------------------------------------------------
# Driver
# ---------------------------------------------------------------------------


def main(results_dir: str = "results", out: str = "docs/tables") -> int:
    results_root = Path(results_dir)
    if not results_root.is_absolute():
        results_root = project_root() / results_root
    out_dir = Path(out)
    if not out_dir.is_absolute():
        out_dir = project_root() / out_dir
    out_dir.mkdir(parents=True, exist_ok=True)

    log_section(f"make_tables: results={results_root}  out={out_dir}")
    artifacts: list[dict] = []

    pairs = [
        ("r1", "t1_audit_decomposition.tex", write_t1_audit_decomposition),
        ("r2", "t2_redundancy_law.tex", write_t2_redundancy),
        ("r3", "t3_responsibility.tex", write_t3_responsibility),
        ("r4", "t4_risk_privacy.tex", write_t4_risk_privacy),
        ("r5", "t5_overhead.tex", write_t5_overhead),
    ]
    for rid, fname, fn in pairs:
        run = _latest_run(results_root, rid)
        if run is None:
            log_info(f"  skip {rid}: no run found")
            continue
        log_info(f"  {rid} run: {run.name}")
        target = out_dir / fname
        try:
            fn(run, target)
            artifacts.append({"name": fname, "source": str(run / f"{rid}.json")})
        except Exception as e:
            log_info(f"    ERROR writing {fname}: {e}")

    manifest = {"tables": artifacts, "out_dir": str(out_dir)}
    with (out_dir / "manifest.json").open("w") as fh:
        json.dump(manifest, fh, indent=2)
    log_info(f"Wrote {len(artifacts)} tables + manifest.json")
    return 0


def parse_args() -> argparse.Namespace:
    p = argparse.ArgumentParser()
    p.add_argument("--results-dir", default="results")
    p.add_argument("--out", default="docs/tables")
    return p.parse_args()


if __name__ == "__main__":
    a = parse_args()
    sys.exit(main(results_dir=a.results_dir, out=a.out))
